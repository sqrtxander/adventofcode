def solve(inp):
    data = inp.strip().splitlines()

    x = 1
    xs = [x]
    for line in data:
        if line == 'noop':
            xs.append(x)
        elif line.startswith('addx'):
            xs.append(x)
            x += int(line.split(' ')[1])
            xs.append(x)

    return sum(xs[i] * (i + 1) for i in range(19, 220, 40))


def main():
    test = solve(TEST_INPUT)
    assert test == EXPECTED, f'Got {test} should be {EXPECTED}'

    with open(INPUT_TXT, 'r') as f:
        print(solve(f.read()))


if __name__ == '__main__':
    main()
